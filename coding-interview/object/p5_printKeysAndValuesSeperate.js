let person = {
    name: "Virat Kohli",
    age: 34,
    profession: "cricketer"
}

//all the keys return as an array
let personKeys = Object.keys(person);
console.log(personKeys);//[ 'name', 'age', 'profession' ]

//all the values return as an array
let personValaues = Object.values(person);
console.log(personValaues);//[ 'Virat Kohli', 34, 'cricketer' ]



